//Java program for 5 prints of "Welcome to Java"//

public class WelcomeToJava {
    public static void main(String[] args) {
        int count = 0;
        //While sttatement to ensure string runs for only 5 prints//
        while (count < 5) {
            System.out.println("Welcome to Java");
            count++;
        }
    }
}
